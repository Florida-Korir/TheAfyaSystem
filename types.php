<h2><br>Skin Cancer<br></h2>
It occurs when there is an abnormal growth of skin cells. It normally forms in areas exposed to the sun but in some instances it can appear to unexposed areas as well. The two main types of skin cancer are Keratinocyte carcinoma and Melanoma. 
<h3>Symptoms of Skin Cancer</h3>
<ul><li>Appearance of skin lesions which may include moles, sore, patch, dark spots or unusual growth that does not go away</li></ul>
<ul><li>Assymetry of the skin lesions which means that two halves of the moles or patch are not identical</li></ul>
<ul><li>The lesions will have ragged or uneven edges</li></ul>
<ul><li>The mole on the skin will have an unusual colour ex. pink and red</li></ul>
<ul><li>The mole is bigger than one quarter inch</li></ul>
<ul><li>One will notice changes in the mole in size, colour and shape</li></ul>
<h2><br>Anal Cancer<br></h2>
Anal cancer is a type of cancer that occurs in the anus, often linked to human papillomavirus (HPV) infection. Common symptom of anal cancer is bleeding from the anus or rectum, anal itching, discharge from the anus, and lump near the anus. Treatments for anal cancer can be chemotherapy, radiation therapy and surgery.
<h2><br>Angiosarcoma<br></h2>
Angiosarcoma is a rare type of cancer that lines the walls of blood vessels or lymphatic vessels. Commonly known cause of angiosarcoma is lymphedema. Symptoms of angiosarcoma can look like a skin infection, a lesion or a bruise that does not heal. Surgery, chemotherapy, radiation therapy are the methods of treatment for angiosarcoma.
<h2><br>Bladder Cancer<br></h2>
Bladder cancer comprises about 5% of all new cancers in the United States, and although it is more common in men than women, incidence rates have been declining. If you or someone you know has bladder cancer, it helps to know the most common signs and symptoms, as well as risk factors, the best path to diagnosis, and how to manage your treatment options.
