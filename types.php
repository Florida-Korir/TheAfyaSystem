<h2><br>DCIS<br></h2>
Ductal carcinoma in situ (DCIS) is the most common type of noninvasive breast cancer in women, where abnormal cells are found in the lining of the breast milk duct. With DCIS, the cancer cells are inside the milk ducts and have not spread in to the fatty breast tissue or to any other part of the body. Treatment for DCIS includes lumpectomy with radiation therapy and mastectomy.
<h2><br>Lymphoma<br></h2>
Lymphoma also termed as lymphatic cancer affects white blood cells known as lymphocytes, the cells that regulates immune system. Symptoms are usually enlargement of lymph nodes, often in the neck, groin, abdomen or armpits. Two main types of lymphoma include hodgkin lymphoma and non-Hodgkin lymphoma that can occur in both children as well as adults.
<h2><br>Liver Cancer<br></h2>
Liver cancer, also known as hepatic cancer happens when normal cells in the liver become abnormal in their appearance and behavior. The exact cause of liver cancer is unknown but is generally associated with damage and scarring of the liver known as cirrhosis. Treatment for liver cancer includes surgery and liver transplant.
<h2><br>Kidney Cancer<Br></h2>
The kidneys are a pair of bean-shaped organs, each about the size of a fist that are located behind your abdominal organs, with one kidney on each side of your spine. Kidney cancer (also called renal cancer) is a disease in which kidney cells become malignant and begin to grow out of control, forming a tumor rarely causing signs or symptoms in its early stages.
