<h1>Common Types Of Cancer</h1>
<h2><br>Breast Cancer<br></h2>
Breast cancer is the most common cancer in women in women, besides skin cancer. There are many different types of breast cancer, and some are very rare. The more you know about the risks, symptoms, and treatment options with breast cancer the better you will be able to navigate your options and live as well as possible with this condition.
<h3>Symptoms of Breast Cancer</h3>
<ul><li>Breast Lumps</li></ul>
<ul><li>Transformation of the breasts in either size or shape</li></ul>
<ul><li>Inverted nipples</li></ul>
<ul><li>Change of skin around the breast such as flaking, scaling or peeling of the skin surrounding the nipple area</li></ul>
<ul><li>Having a red skin over the breast</li></ul>
<h2><br>Cervical Cancer<br></h2>
The cervix is the lower part of the uterus that opens into the vagina. Cervical cancer occurs when abnormal cells on the cervix grow uncontrollably and are caused by longstanding infection with one of the HPVs. Some signs of cervical cancer include abnormal vaginal bleeding, vaginal bleeding after sex or after menopause and bleeding or spotting between periods.
<h3>Symptoms of Cervical Cancer</h3>
<ul><li>Pelvic pain or discomfort during sexual encounter</li></ul>
<ul><li>Spotting afer intercouse</li></ul>
<ul><li>Having watery or bloody vaginal discharge that have a foul odour</li></ul>
<h2><br>Colon Cancer<br></h2>
Colon is the large intestine, the final part of your digestive tract. Colon cancer happens when out-of-control cell growth occurs in the large intestine. It is the third most common type of cancer in the United States. The earliest stages of colon cancer have no symptoms, but this gradually changes as the cancer advances.
<h3>Symptoms of Colon Cancer</h3>
<ul><li>Changes in ones bowel movements including diarrhea, constipation or inconsistency of the stool lasting for more than a month</li></ul>
<ul><li>Traces of blood in stool</li></ul>
<ul><li>Stubborn abdominal discomfort in the form of cramps, pain or gas</li></ul>
<ul><li>Changes in the voice including horseness or not speaking audibly</li></ul>
<ul><li>Loss of weight</li></ul>
<ul><li>Weakness or fatigue</li></ul>
<ul><li>Episodes when you feel that your bowel has not emptied completely</li></ul>
<h2><br>Throat Cancer<br></h2>
It is the type of cancer where cancerous cells grow and develop within the throat, voice box or tonsils. Kenya has the leading number of throat cancer incidences in both men and women globally.
<h3>Symptoms of Throat Cancer</h3>
<ul><li>Soreness of the throat</li></ul>
<ul><li>Ear Pain</li></ul>
<ul><li>A cough</li></ul>
<ul><li>Changes in the voice including horseness or not speaking audibly</li></ul>
<ul><li>Difficulty in swallowing</li></ul>
<ul><li>Weight loss</li></ul>
<ul><li>A lump or sore that never heals</li></ul>
<h2><br>Pancreatic Cancer<br></h2>
It is one in which the cancerous cells develop in the pancreas. When left untreated, the tumour spreads from the pancreas to other adjacent organs to the pancreas like the liver and lungs.During the early stages, it will not exhibit any signs or symptoms making it difficult for ealy detection.
<h3>Symptoms of Pancreatic Cancer</h3>
<ul><li>Abdominal and back discomfort</li></ul>
<ul><li>Unexplained weight loss</li></ul>
