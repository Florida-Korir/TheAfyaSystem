<h2><Br>Leukemia<Br></h2>
Leukemia is a type of cancer that affects the blood, the bone marrow and is caused by the production of abnormal white blood cells. Poor blood clotting, weak immune system and anemia are some common symptoms of leukemia. It can either be acute (fast growing and life threatening) or chronic (slow growing).
<h3>Symptoms of Leukemia</h3>
<ul><li>Common or severe infections since white blood cells are not working properly.</li></ul>
<ul><li>Weakness and fatigue</li></ul>
<ul><li>Swollen lymph nodes or enlarged spleen or liver</li></ul>
<ul><li>Experiencing fever or chills</li></ul>
<ul><li>Unexplained weight loss</li></ul>
<ul><li>Reccuring nose bleeds</li></ul>
<ul><li>Appearance of tiny red patches on the skin</li></ul>
<ul><li>Uncontrollabe sweating at night</li></ul>
<ul><li>Bone pain and soreness</li></ul>
<h2><Br>Lung Cancer<br></h2>
Lung cancer is one of the most common cancers in the world and a leading cause of cancer death. Cigarette smoking causes most lung cancers. Two different types of lung cancer include small cell lung cancer and non-small cell lung cancer. Common lung cancer symptoms include shortness of breath, wheezing, coughing and bloody mucus. Treatment includes surgery, radiation and chemotherapy.
<h3>Symptoms of Lung Cancer</h3>
<ul><li>A bad cough that does not heal</li></ul>
<ul><li>Coughing up blood</li></ul>
<ul><li>Horseness in ones voice</li></ul>
<ul><li>Bone pain</li></ul>
<ul><li>Chest pain</li></ul>
<ul><li>Weight loss</li></ul>
<ul><li>Shortness of breath</li></ul>
<ul><li>Recurrent headaches</li></ul>
