         <h1>Causes:</h1>
Heart disease is caused by damage to all or part of the heart, damage to the coronary arteries or a poor supply of nutrients and oxygen to the organ.<br>
Some types of heart disease, such as hypertrophic cardiomyopathy, are genetic. These, alongside congenital heart defects, can occur before a person is born.<br>
There are a number of lifestyle choices that can increase the risk of heart disease. These include:<br>
• high blood pressure and cholesterol<br>
• smoking<br>
• overweight and obesity <br>
• diabetes<br>
• family history<br>
• a diet of junk food <br>
• age<br>
• a history of preeclampsia during pregnancy<br>
• staying in a stationary position for extended periods of time, such as sitting at work<br>
Having any of these risk factors greatly increases the risk of heart disease. Some, such as age, are unavoidable. For example, once a woman reaches 55 years of age, heart disease becomes more likely.<br>


