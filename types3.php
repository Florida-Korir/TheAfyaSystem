               <h1> Symptoms: </h1><br>
Symptoms depend on which condition is affecting an individual.
However, common symptoms include:<br>
<h4>• Chest pain;</h4> the one common to many types of heart diseases is known as angina or angina pectoris and occurs when part of the heart does not receive enough oxygen. Angina can be triggered by stressful events or physical exertion and normally lasts under 10 minutes.<br>
<h4>• Breathlessness<br></h4>
<h4>• Heart palpitations<br></h4>
Heart attacks can also occur as a result of different types of heart disease. The signs of a heart attack are similar to angina except that they can occur during rest and tend to be more severe. Symptoms of a heart attack can sometimes resemble indigestion, heartburn and a stomach ache can occur, as well as a heavy feeling in the chest. Other symptoms of a heart attack include:<br>
<h4>• pain that travels through the body</h4>, for example from the chest to the arms, neck, back, abdomen, or jaw<br>
<h4>• lightheadedness and dizzy sensations<br></h4>
<h4>• profuse sweating,</h4> nausea and vomiting<br></h4>
