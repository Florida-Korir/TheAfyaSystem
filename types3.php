      <h1>1.  ARRHYTHMIA</h1><br>
Arrhythmia is an irregular heartbeat. There are several ways in which a heartbeat can lose its regular rhythm. These include:<br>
• tachycardia, when the heart beats too fast <br>
• bradycardia, when the heart beats too slowly<br>
• premature ventricular contractions, or additional, abnormal beats<br>
• fibrillation, when the heartbeat is irregular<br>
Arrhythmias occur when the electrical impulses in the heart that coordinate the heartbeat do not work properly making the heart beat in a way it should not. Arrhythmias can become fatal.<br>
        <h1>2. CONGENITAL HEART DISEASE</h1><br>
This is a general term for some deformities of the heart that have been present since birth. Examples include:<br>
• Septal defects: There is a hole between the two chambers of the heart.<br>
• Obstruction defects: The flow of blood through various chambers of the heart is partially or totally blocked.<br>
• Cyanotic heart disease: A defect in the heart causes a shortage of oxygen around the body.<br>


 <h1>3.  CORONARY ARTERY DISEASE</h1><br>
The coronary arteries supply the heart muscle with nutrients and oxygen by circulating blood. Coronary arteries can become diseased or damaged, usually because of plaque deposits that contain cholesterol. Plaque buildup narrows the coronary arteries, and this causes the heart to receive less oxygen and nutrients.<br>

 <h1>4.  DILATED CARDIOMYOPATHY</h1><br>
The heart chambers become dilated as a result of heart muscle weakness and cannot pump blood properly. The most common reason is that not enough oxygen reaches the heart muscle, due to coronary artery disease. This usually affects the left ventricle.<br>
 <h1>5.  MYOCARDIAL I NFARCTION</h1><br>
This is also known as a heart attack, cardiac infarction and coronary thrombosis. An interrupted blood flow damages or destroys part of the heart muscle. This is usually caused by a blood clot that develops in one of the coronary arteries and can also occur if an artery suddenly narrows or spasms.<br>
 <h1>6.  HEART FAILURE</h1><br>
Also known as congestive heart failure, occurs when the heart does not pump blood around the body efficiently. The left or right side of the heart might be affected. Rarely, both sides are. Coronary artery disease or high blood pressure can, over time, leave the heart too stiff or weak to fill and pump properly.<br>


 <h1>7.  HYPERTROPHIC CARDIOMYOPATHY</h1> <br>
This is a genetic disorder in which the wall of the left ventricle thickens, making it harder for blood to be pumped out of the heart. This is the leading cause of sudden death in athletes. A parent with hypertrophic cardiomyopathy has a 50% chance of passing the disorder on to their children.<br>
 <h1>8.  MITRAL REGURGITATION </h1><br>
Also known as mitral valve regurgitation, mitral insufficiency or mitral incompetence. This occurs when the mitral valve in the heart does not close tightly enough. This allows blood to flow back into the heart when it should leave. As a result, blood cannot move through the heart or the body efficiently. People with this type of heart condition often feel tired and out of breath.<br>
 <h1>9.  MITRAL VALVE PROLAPSE</h1><br> 
The valve between the left atrium and left ventricle does not fully close, it bulges upwards, or back into the atrium. In most people, the condition is not life-threatening, and no treatment is required. Some people, especially if the condition is marked by mitral regurgitation, may require treatment.<br>
 <h1>10. PULMONARY STENOSIS </h1><br>
It becomes hard for the heart to pump blood from the right ventricle into the pulmonary artery because the pulmonary valve is too tight. The right ventricle has to work harder to overcome the obstruction. An infant with severe stenosis can turn blue. Older children will generally have no symptoms. Treatment is needed if the pressure in the right ventricle is too high, and a balloon valvuloplasty or open-heart surgery may be performed to clear an obstruction.<br>


