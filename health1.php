        <h4><font color="orange"><b>Healthy living lifestyle</b></font></h4> 
        <P>
        Healthy nutrition, knowing what and when to eat, is an important part of diabetes management as different foods affect your blood glucose levels differently. A healthy diet for all people with diabetes includes reducing the amount of calories if you are overweight, replacing saturated fats (e.g. cream, cheese and butter) with unsaturated fats (e.g. avocado, nuts, olive and vegetable oils), eating dietary fiber (e.g. fruit, vegetables and whole grains) and avoiding tobacco use, excessive alcohol and added sugar.
        The cornerstone of managing type 2 diabetes is a healthy lifestyle, which includes a healthy diet, regular physical activity, not smoking, and maintaining a healthy body weight. Regular physical activity is essential to help keep blood glucose levels under control. 
Over time, a healthy lifestyle may not be enough to keep blood glucose levels under control and people with type 2 diabetes may need to take oral medication. If treatment with a single medication is not sufficient, combination therapy options may be prescribed.
When oral medication is not sufficient to control blood glucose levels, people with type 2 diabetes may require insulin injections.


