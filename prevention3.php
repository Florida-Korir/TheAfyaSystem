        <h2><font color="violet"><b>Prevention methods</b></font></h2>
   Some types of heart disease, such as those that are present from birth, cannot be prevented.<br>
Other types, however, can be prevented by taking the following measures:<br>
• Eat a balanced diet. Stick to low-fat, high-fiber foods and be sure to consume five portions of fresh fruit and vegetables each day. Increase your intake of whole grains and reduce the amount of salt and sugar in the diet. Make sure the fats in the diet are mostly unsaturated.<br>
• Exercise regularly. This will strengthen the heart and circulatory system, reduce cholesterol, and maintain blood pressure.<br>
• Maintain a healthy body weight for your height.<br>
• If you smoke, quit. Smoking is a major risk factor for heart and cardiovascular conditions.<br>
• Reduce the intake of alcohol. Do not drink more than 14 units per week.<br>
• Control conditions that affect heart health as a complication, such as high blood pressure or diabetes.<br>
