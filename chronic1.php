                 It causes breathlessness, or the inability to exhale normally. People usually experience symptoms, including shortness of breath, and normally cough up sputum (mucus from the lungs), especially in the morning. COPD can be tricky for some people to identify, because symptoms are often mistaken for the gradual aging process and body deterioration. In fact, it can develop over the course of several years without any signs of shortness of breath.  It is generally associated with cigarette smoking.
