        <P>
         consume five portions of fresh fruit and vegetables each day. Increase your intake of whole grains and reduce the amount of salt and sugar in the diet. Make sure the fats in the diet are mostly unsaturated.<br>
         Eating a variety of foods is beneficial to our health and can help reduce our risk of disease (including heart disease). Try to eat a wide variety of foods from each of the five food groups, in the amounts recommended. Not only does this help you maintain a healthy and interesting diet, but it provides essential nutrients to the body. <br>

The Heart Foundation recommends: <br>

Plenty of vegetables, fruits and wholegrains.<br>
A variety of healthy protein sources (especially fish and seafood), legumes (such as beans and lentils), nuts and seeds. Smaller amounts of eggs and lean poultry can also be included in a heart healthy diet. If choosing red meat, make sure it  is lean and limit to 1-3 times a week.<br>
Unflavoured milk, yoghurt and cheese. Those with high blood cholesterol should choose reduced fat varieties.<br>
Healthy fat choices –  nuts, seeds, avocados, olives and their oils for cooking<br>
Herbs and spices to flavour foods, instead of adding salt.<br>
Also, be mindful on how much you are eating and whether you are filling up on unhealthy foods. Portion sizes have increased over time and many of us are eating more than we need which can lead to obesity and increase our risk of cardiovascular disease. <br>

Ideally, a healthy plate would include servings of  – ¼ protein, ¼ carbohydrates and ½ vegetables.
